    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try
        {
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/restaurent","root","root");
            String SQL = "SELECT * FROM MENU_DETAILS";
            Statement stRead = conn.createStatement();
            ResultSet rRead = stRead.executeQuery(SQL);
            int itemPos = jComboBox1.getSelectedIndex();
            String item = jComboBox1.getItemAt(itemPos);
            String input = jTextField1.getText();
            int iInput = 0;
            if(input.equalsIgnoreCase(""))
                iInput = 0;
            else
                iInput = Integer.parseInt(input);
            int itemID=0;
            while(rRead.next())
            {
                if(rRead.getString("FOOD_NAME").equalsIgnoreCase(item))
                {
                    itemID = rRead.getInt("FOOD_ID");
                    break;
                }
            }
            rRead.close();
            String SQL2 = "SELECT * FROM INVENTORY";
            rRead = stRead.executeQuery(SQL2);
            while(rRead.next())
            {
                if(rRead.getInt("ITEM_ID")==itemID)
                {
                    int qty = rRead.getInt("QTY");
                    int newQty = qty-iInput;
                    String lQty = String.valueOf(newQty);
                    jLabel5.setText(lQty);
                }
            }
            conn.close();
        }
        catch(Exception e)
        {
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

