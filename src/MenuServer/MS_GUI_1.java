    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int itemPos = jComboBox1.getSelectedIndex();
        itemName = jComboBox1.getItemAt(itemPos);
        String sQty = jTextField1.getText();
        boolean qtyCheck = true;
        boolean flag = false;
        double total =0.0;
        try
        {
            qty = Integer.parseInt(sQty);
        }
        catch(Exception e)
        {
            qtyCheck = false;
        }
        if(qtyCheck)
        {
            try
            {
                Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/restaurent","root","root");
                Statement stRead = conn.createStatement();
                ResultSet rRead = stRead.executeQuery("SELECT * from MENU_DETAILS");
                while(rRead.next())
                {
                    if(rRead.getString("FOOD_NAME").equalsIgnoreCase(itemName))
                    {
                        itemID = rRead.getInt("FOOD_ID");
                        price = rRead.getString("FOOD_PRICE");
                        Statement stRead2 = conn.createStatement();
                        String SQL = "SELECT * FROM INVENTORY";
                        ResultSet rRead2 = stRead2.executeQuery(SQL);
                        while(rRead2.next())
                        {
                            if(rRead2.getInt("ITEM_ID")==itemID)
                            {
                                int checkQty = rRead2.getInt("QTY");
                                if(qty<=checkQty)
                                    flag = true;
                                else
                                    JOptionPane.showMessageDialog(this, "You have entered more quantity than available...!. Please check quantity");
                            }
                        }
                    }
                }
                conn.close();
            }
            catch(Exception e)
            {

            }
        }
        else
            JOptionPane.showMessageDialog(null, "Enter correct qty......!");
        if(flag)
        {
            double dPrice = Double.parseDouble(price);
            total = dPrice*qty;
            String sTotal = String.valueOf(total);
            String data[] = {itemName,sQty,price,sTotal};
            items.add(String.valueOf(itemID));
            items.add(itemName);
            items.add(sQty);
            items.add(price);
            DefaultTableModel tb = (DefaultTableModel)jTable2.getModel();
            tb.addRow(data);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

